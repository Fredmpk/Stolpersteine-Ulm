import type { Metadata } from "next";
import "./globals.css";
import "leaflet/dist/leaflet.css";
import { Fira_Sans } from "next/font/google";
import { VisualEditing } from "next-sanity/visual-editing";
import { DisableDraftMode } from "@/components/DisableDraftMode";
import { draftMode } from "next/headers";
import { sanityProductionFetch } from "@/sanity/lib/client";
import { SanityLive } from "@/sanity/lib/live";
import { Sidebar } from "./components/sidebar";
import { Header } from "./components/header";
import Image from "next/image";
import { BACKGROUNDS_QUERY } from "@/sanity/lib/queries";
import { BACKGROUNDS_QUERYResult } from "@/sanity/types";
import { BurgerMenu } from "./components/burger-menu";

export const metadata: Metadata = {
  title: "Stolpersteine",
  description: "Generated by create next app",
};

const firaSans = Fira_Sans({
  subsets: ["latin"],
  weight: ["300", "400", "500", "600", "700"],
  style: ["normal", "italic"],
  display: "swap",
});

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const backgrounds = await sanityProductionFetch<BACKGROUNDS_QUERYResult>(
    BACKGROUNDS_QUERY,
    {},
    ["backgrounds"],
  );
  {
    return (
      <html lang="en">
        <body
          className={`${firaSans.className} w-5/6 xl:w-4/6 mx-auto min-h-screen flex flex-col`}
        >
          <div className=" pt-4">
            <Header />
          </div>
          <BurgerMenu backgrounds={backgrounds} />
          <div className="flex-row flex">
            <Sidebar backgrounds={backgrounds} />
            <main className="flex-1">{children}</main>
          </div>
          <footer className="relative w-full h-8 sm:h-12 md:h-14 lg:h-16 xl:h-20 mt-auto">
            <Image
              src="/footer_stolpersteine.png"
              alt="Footer"
              fill
              className="object-contain object-center"
            />
          </footer>
          <SanityLive />
          {(await draftMode()).isEnabled && (
            <>
              <VisualEditing />
              <DisableDraftMode />
            </>
          )}
        </body>
      </html>
    );
  }
}
